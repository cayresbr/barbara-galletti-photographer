// Loading Screen
window.addEventListener("load", () => {
  const loader = document.querySelector(".loader");
  const progress = document.querySelector(".loader-progress");

  if (loader && progress) {
    let width = 0;
    const interval = setInterval(() => {
      width += Math.random() * 30;
      if (width >= 100) {
        width = 100;
        clearInterval(interval);

        gsap.to(progress, { width: "100%", duration: 0.3 });

        setTimeout(() => {
          gsap.to(loader, {
            yPercent: -100,
            duration: 1,
            ease: "power4.inOut",
            onComplete: () => {
              loader.style.display = "none";
              initAnimations();
            },
          });
        }, 500);
      } else {
        gsap.to(progress, { width: width + "%", duration: 0.3 });
      }
    }, 200);
  } else {
    // Se não houver loader, inicializa as animações direto
    initAnimations();
  }
});

// Menu Toggle
const menuToggle = document.querySelector(".menu-toggle");
const fullscreenMenu = document.querySelector(".fullscreen-menu");
const menuLinks = document.querySelectorAll(".menu-link");

if (menuToggle && fullscreenMenu) {
  let menuOpen = false;

  menuToggle.addEventListener("click", () => {
    menuOpen = !menuOpen;
    fullscreenMenu.classList.toggle("active");

    if (menuOpen) {
      gsap.to(menuLinks, {
        opacity: 1,
        y: 0,
        duration: 0.6,
        stagger: 0.1,
        delay: 0.3,
        ease: "power3.out",
      });
    } else {
      gsap.to(menuLinks, {
        opacity: 0,
        y: 50,
        duration: 0.3,
      });
    }
  });

  menuLinks.forEach((link) => {
    link.addEventListener("click", () => {
      menuOpen = false;
      fullscreenMenu.classList.remove("active");
      gsap.to(menuLinks, { opacity: 0, y: 50, duration: 0.3 });
    });
  });
}

// Initialize Animations
function initAnimations() {
  // Hero Animations
  gsap.to(".hero-title span", {
    y: 0,
    opacity: 1,
    duration: 1.2,
    stagger: 0.2,
    ease: "power4.out",
    delay: 0.2,
  });

  gsap.to(".hero-subtitle", {
    opacity: 1,
    y: 0,
    duration: 0.8,
    delay: 0.8,
  });

  gsap.to(".magnetic-btn", {
    opacity: 1,
    duration: 0.8,
    delay: 1,
  });

  gsap.to(".hero-bg img", {
    scale: 1,
    duration: 2,
    ease: "power2.out",
  });

  // Scroll Reveals
  gsap.utils.toArray(".reveal").forEach((element) => {
    gsap.fromTo(
      element,
      { opacity: 0, y: 50 },
      {
        opacity: 1,
        y: 0,
        duration: 1,
        ease: "power3.out",
        scrollTrigger: {
          trigger: element,
          start: "top 85%",
          toggleActions: "play none none reverse",
        },
      },
    );
  });

  // Parallax Text
  gsap.utils.toArray(".parallax-text").forEach((text) => {
    const speed = text.dataset.speed || 0.3;
    gsap.fromTo(
      text,
      { x: "0%" },
      {
        x: "-10%",
        ease: "none",
        scrollTrigger: {
          trigger: ".about",
          start: "top bottom",
          end: "bottom top",
          scrub: 1,
        },
      },
    );
  });

  // Horizontal Gallery Scroll (CORRIGIDO: Verificação de segurança)
  const galleryTrack = document.getElementById("galleryTrack");
  if (galleryTrack) {
    const galleryItems = galleryTrack.querySelectorAll(".gallery-item");
    const totalWidth = galleryItems.length * 420 - window.innerWidth + 200;

    gsap.to(galleryTrack, {
      x: -totalWidth,
      ease: "none",
      scrollTrigger: {
        trigger: ".gallery-wrapper",
        start: "top top",
        end: () => `+=${totalWidth}`,
        scrub: 1,
        pin: true,
        anticipatePin: 1,
      },
    });

    // Gallery items parallax
    galleryItems.forEach((item, i) => {
      const img = item.querySelector("img");
      if (img) {
        gsap.fromTo(
          img,
          { scale: 1.2 },
          {
            scale: 1,
            ease: "none",
            scrollTrigger: {
              trigger: ".gallery-wrapper",
              start: "top top",
              end: () => `+=${totalWidth}`,
              scrub: 1,
            },
          },
        );
      }
    });
  }
}

// Testimonial Drag (CORRIGIDO: Verificação de segurança)
const testimonialTrack = document.getElementById("testimonialTrack");
if (testimonialTrack) {
  let isDown = false;
  let startX;
  let scrollLeft;

  testimonialTrack.addEventListener("mousedown", (e) => {
    isDown = true;
    testimonialTrack.style.cursor = "grabbing";
    startX = e.pageX - testimonialTrack.offsetLeft;
    scrollLeft = testimonialTrack.scrollLeft;
  });

  testimonialTrack.addEventListener("mouseleave", () => {
    isDown = false;
    testimonialTrack.style.cursor = "grab";
  });

  testimonialTrack.addEventListener("mouseup", () => {
    isDown = false;
    testimonialTrack.style.cursor = "grab";
  });

  testimonialTrack.addEventListener("mousemove", (e) => {
    if (!isDown) return;
    e.preventDefault();
    const x = e.pageX - testimonialTrack.offsetLeft;
    const walk = (x - startX) * 2;
    testimonialTrack.scrollLeft = scrollLeft - walk;
  });
}

